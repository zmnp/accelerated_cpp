/* 0-4. Write a program that, when run, writes the Hello, world! program
 * as its output.
 */

#include <iostream>

int main() {
  std::cout << "#include <iostream>\n"
               "int main() {\n"
               "  std::cout << \"Hello, world!\" << std::endl;\n"
               "  return 0;\n}\n";
  return 0;
}
